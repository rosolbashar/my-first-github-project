import turtle
m=turtle.Turtle()
m.pensize(10)
m.pencolor("blue")
m.circle(60)
m.up()
m.goto(128,-1)
m.down()
m.pencolor("black")
m.circle(60)
m.up()
m.goto(190,95)
m.down()
m.pencolor("green")
m.circle(60)
m.up()
m.goto(60,90)
m.down()
m.pencolor("yellow")
m.circle(60)
m.up()
m.goto(-60,90)
m.down()
m.pencolor("red")
m.circle(60)
m.up()
turtle.done()
